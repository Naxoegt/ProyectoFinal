package org.example;

class Asiento {
    private int valor;
    private int tipo;
    private int numero;

    public Asiento(){}

    public int Valor() {
        return valor;
    }

    public int Tipo(){

        return tipo;
    }

    public int Numero(){

        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

}